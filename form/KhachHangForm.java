    private void btnThanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThanhToanActionPerformed
       int row = tblHoaDon.getSelectedRow();

    if(row == -1){
        JOptionPane.showMessageDialog(this,"Chọn hóa đơn cần thanh toán");
        return;
    }

String trangThai = tblHoaDon.getValueAt(row, 2).toString();
String selected = cboPhuongThuc.getSelectedItem().toString();
int maPT = Integer.parseInt(selected.split(" - ")[0]);
    if(trangThai.equals("Đã thanh toán")){
        JOptionPane.showMessageDialog(this,"Hóa đơn này đã thanh toán rồi");
        return;
    }

    int MaHoaDon = Integer.parseInt(tblHoaDon.getValueAt(row, 0).toString());

    try{
        Connection conn = ConnectDB.getConnection();

        String sql = "UPDATE HOA_DON SET TrangThai=1, NgayThanhToan=GETDATE(), MaPT=? WHERE MaHoaDon=?";

        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setInt(1, maPT);
ps.setInt(2, MaHoaDon);

        ps.executeUpdate();

        JOptionPane.showMessageDialog(this,"Thanh toán thành công");

        loadHoaDon(maKH);

    }catch(Exception e){
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnThanhToanActionPerformed

