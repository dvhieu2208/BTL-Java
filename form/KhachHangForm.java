    public void loadHoaDon(int maKH){

    try{
        Connection conn = ConnectDB.getConnection();

        String sql = "SELECT MaHoaDon, ChiSoCu, ChiSoMoi, TrangThai, NgayThanhToan FROM HOA_DON WHERE MaKH=?";

        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setInt(1, maKH);

        ResultSet rs = ps.executeQuery();

        DefaultTableModel model = (DefaultTableModel) tblHoaDon.getModel();
        model.setRowCount(0);

        while(rs.next()){

            int soDien = rs.getInt("ChiSoMoi") - rs.getInt("ChiSoCu");
            int soTien = TinhTienDien.tinhTienDien(soDien);
            model.addRow(new Object[]{
                rs.getInt("MaHoaDon"),
                soDien,
                rs.getInt("TrangThai") == 0 ? "Chưa thanh toán" : "Đã thanh toán",
                rs.getDate("NgayThanhToan"),
                soTien
            });
        }

    }catch(Exception e){
        e.printStackTrace();
    }
}
    public void loadPhuongThuc(){

    try{
        Connection conn = ConnectDB.getConnection();

        String sql = "SELECT DISTINCT MaPT, TenPT FROM PHUONG_THUC_THANH_TOAN";

        PreparedStatement ps = conn.prepareStatement(sql);
        ResultSet rs = ps.executeQuery();

        cboPhuongThuc.removeAllItems();

        while(rs.next()){
            cboPhuongThuc.addItem(rs.getInt("MaPT") + " - " + rs.getString("TenPT"));
        }

    }catch(Exception e){
        e.printStackTrace();
    }
}
    public void loadThongTinKH(int maKH){
    try{
        Connection conn = ConnectDB.getConnection();

        String sql = "SELECT HoTen, SDT, Email FROM KHACH_HANG WHERE MaKH=?";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setInt(1, maKH);

        ResultSet rs = ps.executeQuery();

        if(rs.next()){
            lbHoTen.setText("Họ Tên: " + rs.getString("HoTen"));
            lbSDT.setText("SĐT: " + rs.getString("SDT"));
            lbEmail.setText("Email: " + rs.getString("Email"));
        }

    }catch(Exception e){
        e.printStackTrace();
    }
}
    
