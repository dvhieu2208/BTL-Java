    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = tblTaiKhoan.getSelectedRow();

    if(row == -1){
        JOptionPane.showMessageDialog(this,"Chọn tài khoản cần xóa");
        return;
    }

    int maTK = Integer.parseInt(tblTaiKhoan.getValueAt(row, 0).toString());

    String vaiTro = tblTaiKhoan.getValueAt(row, 3).toString();

    int confirm = JOptionPane.showConfirmDialog(
        this,
        "Bạn có chắc muốn xóa tài khoản này?",
        "Xác nhận",
        JOptionPane.YES_NO_OPTION
    );

    if(confirm != JOptionPane.YES_OPTION){
        return;
    }

    try{
        Connection conn = ConnectDB.getConnection();

        String sql = "UPDATE TAI_KHOAN SET TrangThai = 0 WHERE MaTK=?";

        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setInt(1, maTK);

        ps.executeUpdate();

        JOptionPane.showMessageDialog(this,"Đã khóa tài khoản");

        loadTaiKhoan(); // reload lại bảng

    }catch(Exception e){
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnXoaActionPerformed

